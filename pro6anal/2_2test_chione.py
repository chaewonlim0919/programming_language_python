print("대립 가설 : 기대치와 관찰치는 차이가 있다. 이 주사위는 게임에 적합하지 않다.")
print()

import pandas as pd
import scipy.stats as stats
# 각 눈금에 대한 관측 빈도 수
# df = n -1 = 6 - 1 = 5
# 유의 수준 : 0.05
# 임계값 = 11.07
# 카이제곱값 = 14.2 임계값을 넘기 때문에 귀무기각역에 존재.
data = [4, 6, 17, 16, 8, 9]
print(stats.chisquare(data)) # chi2=14.2, pvalue0.014

# 판정결과 리포트를 적을땐 좀더 장황하게 적어야함.
print("판정 : 유의수준(α):0.05 < pvalue:0.014 이므로 \n" \
"귀무가설을 기각하고 대립가설 채택\n" \
"따라서 검증에 사용된 데이터는 우연히 발생한 것이 아니라 필연적인 원인에 발생한 것이다.\n" \
"이 주사위는 게임에 적합하지 않다라는 의견을 받아 들임")

